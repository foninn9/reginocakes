window.addEventListener('scroll', function(){
    let animation = document.getElementById('cake1');
    let position = animation.getBoundingClientRect().top;
    console.log(position);
    let pantalla= window.innerHeight/2;
    if(position < pantalla){
        animation.style.animation = 'mover 1s ease-out'
    }
})
